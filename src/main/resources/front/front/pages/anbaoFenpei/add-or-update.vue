			// 日期控件
			anpaiTimeConfirm(val) {
                this.ruleForm.anpaiTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			zhxingTimeConfirm(val) {
                this.ruleForm.zhxingTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },




            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.anbaoFenpeiName)) {
                    this.$utils.msg(`安排名称不能为空`);
                    return
                }
				if ((!this.ruleForm.anbaoFenpei)) {
                    this.$utils.msg(`安排内容不能为空`);
                    return
                }
				if ((!this.ruleForm.anpaiTime)) {
                    this.$utils.msg(`安排时间不能为空`);
                    return
                }
				if ((!this.ruleForm.zhxingTime)) {
                    this.$utils.msg(`执行时间不能为空`);
                    return
                }
				if ((!this.ruleForm.anbaoFenpeiXiangqing)) {
                    this.$utils.msg(`安排详情不能为空`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`anbaoFenpei`, this.ruleForm);
                } else {
                    await this.$api.add(`anbaoFenpei`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
