        },
        async onLoad(options) {

		/*下拉框*/
			let anbaoFenpeiTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'anbao_fenpei_types',
            }
			let anbaoFenpeiTypes = await this.$api.page(`dictionary`, anbaoFenpeiTypesParams);
			this.anbaoFenpeiTypesOptions = anbaoFenpeiTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`anbaoFenpei`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.anbaoFenpeiId){
                this.ruleForm.anbaoFenpeiId = options.anbaoFenpeiId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            // 下拉变化
            anbaoFenpeiTypesChange(e) {
                this.anbaoFenpeiTypesIndex = e.target.value
                this.ruleForm.anbaoFenpeiValue = this.anbaoFenpeiTypesOptions[this.anbaoFenpeiTypesIndex].indexName
                this.ruleForm.anbaoFenpeiTypes = this.anbaoFenpeiTypesOptions[this.anbaoFenpeiTypesIndex].codeIndex
            },


            // styleChange() {
            // 	this.$nextTick(() => {
